<?php


namespace grozzzny\catalog\models;


use yii\base\DynamicModel;
use yii\easyii\helpers\Image;
use yii\easyii\helpers\Upload;
use yii\helpers\ArrayHelper;
use yii\web\UploadedFile;

class DataProperties extends DynamicModel
{
    const PRIMARY_MODEL = false;

    private $labels = [];
    private $types = [];
    private $settings = [];
    private $options = [];

    public function __construct(array $properties = [], array $data = [], $config = [])
    {
        $attributes = [];
        $values = [];

        foreach ($data as $data_property){

            $value = $data_property->value;

            if($data_property->property->type == Properties::TYPE_MULTICATEGORY){

                if(empty($old_category_id[$data_property->property_slug])){
                    $old_category_id[$data_property->property_slug] = $value;
                    $values[$data_property->property_slug][] = $value;
                }

                if($value > $old_category_id[$data_property->property_slug]) {
                    $values[$data_property->property_slug][] = $value;
                }

                $old_category_id[$data_property->property_slug] = $value;

            }elseif($data_property->property->settings->multiple){
                $values[$data_property->property_slug][] = $value;
            }else{
                $values[$data_property->property_slug] = $value;
            }
        }

        foreach ($properties as $property){
            $attributes[$property->slug] = $property->settings->multiple ? [] : null;

            if (!empty(ArrayHelper::getValue($values, $property->slug, null))) $attributes[$property->slug] = $values[$property->slug];

            $this->labels[$property->slug] = $property->title;
            $this->types[$property->slug] = $property->type;
            $this->settings[$property->slug] = $property->settings;
            $this->options[$property->slug] = $property->options;

            foreach ($property->validations as $validation){
                $validator = $validation[0];
                $params = ArrayHelper::getValue($validation, '1', []);
                $this->addRule($property->slug, $validator, $params);
            }

        }

        parent::__construct($attributes, $config);
    }

    public function getType($slug)
    {
        return $this->types[$slug];
    }

    public function getSettings($slug)
    {
        return $this->settings[$slug];
    }

    public function getOptions($slug)
    {
        return $this->options[$slug];
    }

    /**
     * @return array
     */
    public function attributeLabels()
    {
        return $this->labels;
    }

    public function setAttributes($values, $safeOnly = true)
    {
        $new_values = [];
        if (is_array($values)) {
            foreach ($values as $name => $value) {
                if($this->getType($name) == Properties::TYPE_MULTICATEGORY) {
                    $values = (is_array($value)) ? $value : [$value];
                    $categories_arr = [];
                    foreach ($values as $val) {
                        $categories_arr = ArrayHelper::merge($categories_arr, [$val] + Category::getOnlyParentId($val));
                    }
                    $new_values[$name] = $categories_arr;
                }else{
                    $new_values[$name] = $value;
                }
            }
        }
        parent::setAttributes($new_values, $safeOnly); // TODO: Change the autogenerated stub
    }



    public static function queryFilter(&$query, $get, $properties){

        $filtersApplied = 0;
        $subQuery = Data::find()->select('item_id, COUNT(*) as filter_matched')->groupBy('item_id');

        foreach ($properties as $property){

            if(!$property->settings->filter_show) continue;

            if ($property->settings->filter_range){

                $value_from = ArrayHelper::getValue($get, $property->slug . '_from', '');
                $value_to = ArrayHelper::getValue($get, $property->slug . '_to', '');

                if(empty($value_from) && empty($value_to)) continue;

                if(!$value_from){
                    $additionalCondition = ['<=', 'value', (int)$value_to];
                } elseif(!$value_to) {
                    $additionalCondition = ['>=', 'value', (int)$value_from];
                } else {
                    $additionalCondition = ['between', 'value', (int)$value_from, (int)$value_to];
                }

                $subQuery->orFilterWhere(['and', ['property_slug' => $property->slug], $additionalCondition]);

            }else{
                $value = ArrayHelper::getValue($get, $property->slug, '');

                if(empty($value)) continue;

                switch ($property->type){
                    case Properties::TYPE_DATETIME:
                        $subQuery->orFilterWhere(['and', ['property_slug' => $property->slug], ['=', 'FROM_UNIXTIME(`value`,\'%Y-%m-%d\')', date('Y-m-d',$value)]]);
                        break;
                    case Properties::TYPE_CHECKBOX:
                    case Properties::TYPE_FILE:
                    case Properties::TYPE_IMAGE:
                        $subQuery->orFilterWhere(['and', ['property_slug' => $property->slug], ['not', ['value' => null]]]);
                        break;
                    default:
                        $subQuery->orFilterWhere(['and', ['property_slug' => $property->slug], ['value' => $value]]);
                }
            }
            $filtersApplied++;
        }

        if($filtersApplied) {
            $query->join('LEFT JOIN', ['f' => $subQuery], 'f.item_id = gr_catalog_items.id');
            $query->andFilterWhere(['f.filter_matched' => $filtersApplied]);
        }
    }

}